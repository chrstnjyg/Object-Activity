// Create an object called car with the properties type,model, and color.
let car = {
    type: "Sedan",
    model: "Corolla",
    color: "Red Mica Metallic"
};

// Use typeof to check the type of the object and log it to the console. 
console.log(typeof car); 

// Update the type property to "Toyota" and log the updated object.
car.type = "Toyota";
console.log("Car type:", car.type);

// Add new property wheels with the value 4 and log the updated object.
car.wheels = 4;
console.log("Car wheels:", car.wheels);